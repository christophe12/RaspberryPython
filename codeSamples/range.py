# First possible use of a range

for i in range (5):
	print (i)

# Printing a separator line
print("---------------------------------------------------------------------------------")

# Second possible use of a range

for i in range (2, 5):
	print (i)

# Printing a separator line
print("---------------------------------------------------------------------------------")

# Using variables in a range

start_number = 5
stop_number = 10

for i in range (start_number, stop_number):
	print (i)

# Printing a separator line
print("---------------------------------------------------------------------------------")

# Using a step number in a range function : syntax -> range(start, stop, step)

for i in range (2, 9, 2):
	print (i)

# Printing a separator line
print("---------------------------------------------------------------------------------")

# Stepping backward

for i in range (8, 1, -2):
	print (i)

# NB: Notice that the ending number is not printed